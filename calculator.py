class Calculator:

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def plus(self):
        return self.a + self.b

    def umnog(self):
        return self.a * self.b

    def delit(self):
        return self.a / self.b

    def minus(self):
        return self.a - self.b

